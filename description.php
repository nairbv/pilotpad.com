Welcome to <a href="http://pilotpad.com">PilotPad.com</a>, a simple notepad where you can keep some notes. Think of PilotPad as an extension to cut/paste, an extra clipboard, a place to copy down a URL or a quote that you might want to look at later or on another computer.
<br />
<br />
Do you ever email something to yourself? Open a draft email just to save for later? Try Pilotpad instead. <br /> 
<br />
Your notes here are private, and are accessible from any web browser. Simplicity and ease of use are the goals; you don't even need to remember another password.
<br /> <br />
Notes kept here are saved online automatically every ten seconds, otherwise be sure to click the "save" button.
<br/><br/>
Currently limited to 5MB of text (should be plenty for pasting normal snippets of text).
